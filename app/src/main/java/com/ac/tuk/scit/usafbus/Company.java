package com.ac.tuk.scit.usafbus;

public class Company {

    String image, name;


    public Company() {

    }

    public Company(String image, String name) {
        this.image = image;
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
